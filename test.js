function buildHtmlTable(index,selector) {
  $(selector).empty()
  var criteria_selected = criteria[Number(index)]
  var values = []
  var columnValues = {}
  for (var i = 0; i < JSONn.length; i++) 
  {
      var data = JSONn[i]
      var xyz = {}
      var red = 0
      var green = 0
      for (var key in criteria_selected)
      {
          if (data[key] >= criteria_selected[key])
          {
              green++
              xyz[key]='lightgreen'
              
          }
          else
          {
              red++
              xyz[key]='#ff0000'
              if (!(key in columnValues))
                {
                    columnValues[key] = 0
                }
                columnValues[key]++
          } 
      }
      xyz['Criteria'] = [green,red]
      values.push(xyz)
  }



  var columns = addAllColumnHeaders(JSONn, selector, columnValues);

  for (var i = 0; i < JSONn.length; i++) {
    var row = $('<tr/>');
    for (var colI = 0; colI < columns.length; colI++) {
      var value = JSONn[i][columns[colI]];
      if (value == null) value = "0";
      var color = 'white'
      
      if (columns[colI] == 'Criteria')
      {
            var green = String(values[i][columns[colI]][0]).fontcolor('green')
            var red = String(values[i][columns[colI]][1]).fontcolor('red')
            row.append($('<td/>').html(green + ' - ' + red )).css("background-color", 'white');
            continue;
      }
      if (criteria_selected.hasOwnProperty(columns[colI]) && Number(criteria_selected[columns[colI]]) !== -1)
            color = values[i][columns[colI]]
      row.append($('<td/>').html(value).css("background-color", color));
    }
    $(selector).append(row);
  }
}

function addAllColumnHeaders(JSONn, selector, columnValues) {
  var columnSet = [];
  var percentage = $('<thead/>');
  var headerTr$ = $('<thead/>');

    var rowHash = JSONn[0];
    for (var key in rowHash)
    {
        if (key in columnValues)
        {
            var green = columnValues[key]
            green = ((green/JSONn.length) * 100).toFixed(2)
            var red = (100 - green).toFixed(2)
            green = (String(columnValues[key]) + "(" + String(green) + " %)").fontcolor('red')
            red = (String(Math.abs(JSONn.length - columnValues[key])) + "(" + String(red) + " %)").fontcolor('green')
            percentage.append($('<th/>').html(red+ green))
        }
        else
            percentage.append($('<th/>'))
        columnSet.push(key);
        headerTr$.append($('<th/>').html(key));
    }
    $(selector).append(percentage);
  $(selector).append(headerTr$);
  return columnSet;
}


